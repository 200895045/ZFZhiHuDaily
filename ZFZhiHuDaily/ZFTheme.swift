import UIKit

class ZFTheme {
    
    let color:Int
    
    let thumbnail:String
    
    let description:String
    
    let id:Int
    
    let name:String
    
    init(color:Int,thumbnail:String,description:String,id:Int,name:String){
        self.color = color
        self.thumbnail = thumbnail
        self.description = description
        self.id = id
        self.name = name
    }

}
